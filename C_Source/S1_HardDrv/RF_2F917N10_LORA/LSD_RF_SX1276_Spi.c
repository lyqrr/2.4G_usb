void SX1276Reset()
{
    LSD_RF_SXRESET_LOW()  ;   //复位引脚输出低电平
    Lib_Delay_ms(1);
    LSD_RF_SXRESET_HIGH() ;  //复位引脚输出高电平
    Lib_Delay_ms(10);
}


//**********************************************************************************
// 功能描述 : spi发送or接收1字节
// 输入参数 : unsigned char mosi
// 返回参数 : unsigned char
// 说明     : 
//**********************************************************************************
unsigned char LSD_RF_SpiInOut (unsigned char mosi) 
{ 
    unsigned char i;
    unsigned char onebyte;
    onebyte = mosi;
    for(i = 8; i > 0; i--) 
    {
        NOP();NOP();
        if(onebyte & 0x80) 
        {
    	   SPI_D0_HIGH();
        }
        else 
        {
    	   SPI_D0_LOW();

        }

        NOP();NOP();
        SPI_CLK_HIGH();
		onebyte <<= 1;      // next bit
//	SPI_DI_Enable();
        if(SPI_DI_JUDGE) 
        {
            onebyte++;      // 1 found on MISO
        }
        NOP();NOP();
        SPI_CLK_LOW();
	}
    return onebyte;         // mosi now becomes the value of miso
}


